#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	unsigned long		i;
	void				*a;

	i = 0;
	a = ft_malloc(size * count);
	while (i < size * count)
	{
		((char *)a)[i] = '\0';
		i++;
	}
	return (a);
}
