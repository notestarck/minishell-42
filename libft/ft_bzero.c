#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned long	i;
	unsigned char	*a;

	a = s;
	if (n <= 0)
		return ;
	i = 0;
	while (i < n)
	{
		a[i] = '\0';
		i++;
	}
}
